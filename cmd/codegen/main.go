package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

func main() {
	repoRoot := findRepoRoot()
	modulesDir := filepath.Join(repoRoot, "modules")

	importSet := make(map[string]struct{})

	filepath.WalkDir(modulesDir, func(path string, d os.DirEntry, err error) error {
		if err != nil || !d.IsDir() {
			return err
		}
		base := d.Name()

		// Skip well-known non-module directories
		if base == "testdata" || base == "vendor" || strings.HasPrefix(base, ".") {
			return filepath.SkipDir
		}

		// Import v1 directories under api that have a register.go (self-registration)
		if base == "v1" && strings.Contains(path, "/api/") {
			if hasFile(path, "register.go") {
				rel, _ := filepath.Rel(repoRoot, path)
				importSet["hei-gin/"+filepath.ToSlash(rel)] = struct{}{}
			}
			return filepath.SkipDir
		}

		return nil
	})

	imports := make([]string, 0, len(importSet))
	for p := range importSet {
		imports = append(imports, p)
	}
	sort.Strings(imports)

	genFile := filepath.Join(repoRoot, "core", "app", "modules_gen.go")
	f, err := os.Create(genFile)
	if err != nil {
		fmt.Fprintf(os.Stderr, "Failed to create %s: %v\n", genFile, err)
		os.Exit(1)
	}
	defer f.Close()

	f.WriteString("// Code generated by cmd/codegen; DO NOT EDIT.\n")
	f.WriteString("package app\n\n")
	f.WriteString("import (\n")

	var lastGroup string
	for _, p := range imports {
		parts := strings.Split(p, "/")
		group := ""
		if len(parts) >= 3 {
			group = parts[2]
		}
		if group != lastGroup && lastGroup != "" {
			f.WriteString("\n")
		}
		lastGroup = group
		f.WriteString(fmt.Sprintf("\t_ %q\n", p))
	}

	f.WriteString(")\n")
	fmt.Printf("Generated %s with %d imports\n", genFile, len(imports))
}

func findRepoRoot() string {
	dir, err := os.Getwd()
	if err != nil {
		fmt.Fprintf(os.Stderr, "Failed to getwd: %v\n", err)
		os.Exit(1)
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			fmt.Fprintf(os.Stderr, "go.mod not found\n")
			os.Exit(1)
		}
		dir = parent
	}
}

func hasFile(dir, name string) bool {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return false
	}
	for _, e := range entries {
		if !e.IsDir() && e.Name() == name {
			return true
		}
	}
	return false
}
